import Foundation
import UIKit

extension UIColor{
    static func AppThemeGrayDisableColor() -> UIColor{
        return #colorLiteral(red: 0.6352941176, green: 0.6352941176, blue: 0.6352941176, alpha: 1)
    }
    
    static func AppThemeGrayTextColor() -> UIColor {
        return #colorLiteral(red: 0.2196078431, green: 0.2196078431, blue: 0.2196078431, alpha: 1)
    }
    static func AppThemeBlackTextColor() -> UIColor {
        return #colorLiteral(red: 0.2196078431, green: 0.2196078431, blue: 0.2196078431, alpha: 1)
    }
    static func AppThemeGreenTextColor() -> UIColor {
        return #colorLiteral(red: 0.3803921569, green: 0.7529411765, blue: 0.5333333333, alpha: 1)
    }
    
    static func AppThemeRedTextColor() -> UIColor {
        return #colorLiteral(red: 0.9254901961, green: 0.2901960784, blue: 0.3254901961, alpha: 1)
    }
    
    static func AppThemeBlueTextColor() -> UIColor {
        return #colorLiteral(red: 0.1098039216, green: 0.6156862745, blue: 0.8352941176, alpha: 1)
    }
    static func AppThemeWhiteTextColor() -> UIColor {
        return #colorLiteral(red: 1, green: 1, blue: 1, alpha: 1)
    }
}
